package org.schema.game.server.data.simulation.npc.geo;

public class NPCBlueprintVersionException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public NPCBlueprintVersionException() {
		super();
	}

	public NPCBlueprintVersionException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public NPCBlueprintVersionException(String message, Throwable cause) {
		super(message, cause);
	}

	public NPCBlueprintVersionException(String message) {
		super(message);
	}

	public NPCBlueprintVersionException(Throwable cause) {
		super(cause);
	}

}
